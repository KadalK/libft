#include "libft.h"

long	ft_atol(const char *str)
{
	int		conv;
	int		i;
	long	res;

	res = 0;
	i = 0;
	conv = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' '))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			conv *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (res * conv);
}

int	ft_atoi(const char *str)
{
	return ((int)ft_atol(str));
}
